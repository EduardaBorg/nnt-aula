package aula_1

class exercicio3 {

    fun questao1() {
        //Para armazenar um número decimal, deve-se utilizar o tipo de dado Double ou Float
        val x: Double = 3.14159
        println("Valor de x: $x")

    }

    fun questa2(){
        //Para armazenar um texto, deve-se utilizar o tipo de dado String. Já para armazenar um número inteiro,
        //pode-se utilizar o tipo de dado Int
        val nome: String = "Maria"
        val idade: Int = 30

        println("Nome: $nome")
        println("Idade: $idade")

    }

    fun questao3(){
        // Para armazenar um número inteiro, deve-se utilizar o tipo de dado Int.
        // Já para armazenar um número decimal, pode-se utilizar o tipo de dado Double ou Float
        val x: Int = 10
        val y: Double = 3.14159

        println("Valor de x: $x")
        println("Valor de y: $y")
    }

}